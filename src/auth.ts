export interface User {
  username: string;
  password?: string; // Mật khẩu không cần gửi ra ngoài
  isAdmin?: boolean;
  expiresAt?: string; // Định dạng ISO: "YYYY-MM-DDTHH:mm:ssZ"
  sessionId?: string; // ID để quản lý phiên đăng nhập duy nhất
}

// ============== HƯỚNG DẪN CẤU HÌNH NGÀY HẾT HẠN =================
//
// Bạn có thể tùy chỉnh ngày hết hạn cho từng tài khoản bằng cách sửa trường `expiresAt`.
//
// CÁCH SỬA:
// 1. Tìm tài khoản bạn muốn thay đổi.
// 2. Thay đổi giá trị của `expiresAt`:
//    - Dùng `defaultExpiryISO`: Tài khoản sẽ hết hạn sau 30 ngày kể từ hôm nay.
//    - Dùng một chuỗi ngày tháng cụ thể: `"YYYY-MM-DDTHH:mm:ssZ"`.
//      Ví dụ: `"2025-12-31T23:59:59Z"` nghĩa là hết hạn vào cuối ngày 31/12/2025 (theo giờ UTC).
//    - Xóa hẳn trường `expiresAt`: Tài khoản sẽ không bao giờ hết hạn (hữu ích cho admin).
//
// VÍ DỤ:
// { username: 'user_A', password: '123', expiresAt: defaultExpiryISO }, // Hết hạn trong 30 ngày
// { username: 'user_B', password: '456', expiresAt: "2025-06-15T23:59:59Z" }, // Hết hạn ngày 15/06/2025
// { username: 'user_C', password: '789' }, // Không bao giờ hết hạn
//
// ======================================================================

// ============== CẤU HÌNH DANH SÁCH TÀI KHOẢN ==============
// Thêm, sửa, hoặc xóa các tài khoản hợp lệ trong danh sách dưới đây.
// Mỗi tài khoản là một đối tượng có `username` và `password`.
const expiryDate = new Date();
expiryDate.setDate(expiryDate.getDate() + 30);
const defaultExpiryISO = expiryDate.toISOString();

